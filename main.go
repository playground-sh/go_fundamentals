package main

import (
	"fmt"
	"go_fundamentals/data_types"
)

func main() {
	// Numeric Types
	data_types.SignedIntegers()
	fmt.Println()
	data_types.UnsignedIntegers()
	fmt.Println()
	data_types.FloatingPoints()
	fmt.Println()
	data_types.ComplexNumbers()
	fmt.Println()
	data_types.DefaultIntegers()
	fmt.Println()

	// Boolean Type
	data_types.BooleanData()
	fmt.Println()

	// String Type
	data_types.StringData()
}
